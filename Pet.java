
public class Pet {
	
	public String name;
	public String type;
	public int age;
	
	public Pet(String name, String type, int age) {
		super();
		this.name = name;
		this.type = type;
		this.age = age;
	}

	public String getName(String name) {
		return name;
	}

	public String getType(String type) {
		return type;
	}

	public int getAge(int age) {
		return age;
	}
	
	

}
